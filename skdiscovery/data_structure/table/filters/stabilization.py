from skdaccess.utilities.pbo_util import stab_sys
from skdiscovery.data_structure.framework.base import PipelineItem

class StabilizationFilter(PipelineItem):
    '''
    This filter transforms GPS stations in a region to a local reference frame
    '''
    def process(self, obj_data):
        ''' 
        Apply stabilization filter to data set
        
        @param obj_data: Table data wrapper.
        '''

        stab_sys(obj_data.getIterator(), obj_data.info())
